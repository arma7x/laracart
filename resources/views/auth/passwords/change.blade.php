            const fields = document.getElementsByClassName('change-password');
            checkbox.addEventListener('change', (evt) => {
                let type = 'text';
                if (evt.target.checked) {
                    type = 'password';
                }
                console.log(type)
                Object.keys(fields).forEach((i) => {
                    if (fields[i].name && fields[i].name.indexOf('password') > -1) {
                        fields[i].setAttribute('type', type);
                    }
                });
            })
